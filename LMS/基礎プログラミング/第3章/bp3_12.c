// 7種類の演算子

#include <stdio.h>

int main(void) {
    printf("20 + 7 = %d\n", 2 + 7);
    printf("20 - 7 = %d\n", 20 - 7);
    printf("20 * 7 = %d\n", 20 * 7);
    printf("20 / 7 = %d\n", 20 / 7);
    printf("20 %% 7 = %d\n", 20 % 7);
    printf("+7 = %d\n", +7);
    printf("-7 = %d\n", -7);
    return (0);
}

